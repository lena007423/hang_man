from tle import Turtle, done
shn = input("Write a shape name: ")
t = Turtle ()
if shn == "circle":
    t.pensize(300)
    t.pencolor("#4A148C")
    t.circle(1)
elif shn == "square":
    t.pensize(100)
    t.pencolor("#FBC02D")
    t.forward(100)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward(100)
elif shn == 'triangle':
    t.pensize(90)
    t.pencolor("#FFA000")
    t.forward(150)
    t.left(120)
    t.forward(150)
    t.left(120)
    t.forward(150)
elif shn =="rectangle":
    t.pensize(100)
    t.pencolor("#0288D1")
    t.forward (200)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward (200)
    t.left(90)
    t.forward(100)
elif shn == 'star':
    t.pensize(10)
    t.pencolor("#0288D1")
    t.left(60)
    t.forward(100)
    t.left(-120)
    t.forward(100)
    t.left(60)
    t.forward(100)
    t.left(-120)
    t.forward(100)
    t.left(60)
    t.forward(100)
    t.left(-120)
    t.forward(100)
    t.left(60)
    t.forward(100)
    t.left(-120)
    t.forward(100)
    t.left(60)
    t.forward(100)
    t.left(-120)
    t.forward(100)
    t.left(60)
    t.forward(100)
    t.left(-120)
    t.forward(100)
done()